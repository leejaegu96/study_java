package com.web.EWord.ch03;

public class AssignmentOperatorExample {

	public static void main(String[] args) {

//		+=
		int result = 10;
		int result2 = 10;
		int result3 = 10;
		
		result += 10;
//		result = result + 10;
		
		System.out.println("result: " + result);

//		-=
		result2 -= 10;
		
		System.out.println("result2: " + result2);
		
//		*=
		result3 *= 10;	
		
		System.out.println("result3: " + result3);
		
	}

}
