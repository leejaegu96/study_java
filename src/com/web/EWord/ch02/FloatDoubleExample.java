package com.web.EWord.ch02;

public class FloatDoubleExample {

	public static void main(String[] args) {
		
//		double이 메모리를 더 차지하지만 정확도가 높다.
		double var1 = 3.14;
		float var2 = 3.14f;
		float var3 = 3.14F;
		
		System.out.println(var1);
		System.out.println(var2);
		System.out.println(var3);
		
	}

}
